#include <iostream>

using namespace std;

class Circle{
    int radius;
    public:
    Circle();
    Circle(int r);
    ~Circle();
    void setRadius(int r){radius = r;}
    double getArea(){return 3.14*radius*radius;}
};

Circle::Circle(){
    radius = 1;
    cout << "생성자 실행 radius = " << radius << '\n';
}
Circle::Circle(int r){
    radius = r;
    cout << "생성자 실행 radius = " << radius << '\n';
}
Circle::~Circle(){
    cout << "소멸자 싱행 radius = " << radius << '\n';
}
int main(){
    int radius;
    while (1){
        cout << "정수 반지름 입력(음수이면 종료) >> ";
        cin >> radius;
        if (radius < 0){
            return 0;
        }
        Circle *p = new Circle(radius); // 동적 메모리 (프로그램 실행중 만들어짐)
        cout << "원의 면적은 " << p->getArea() << '\n';
        delete p;
    }
    return 0;
}